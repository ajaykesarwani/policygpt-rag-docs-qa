import json
from dataclasses import dataclass
from typing import List, Dict, Optional

from app.rag_pipeline import rag_query, build_prompt
from app.models import DocumentChunk
from app.config import get_settings
from openai import OpenAI

settings = get_settings()

# We reuse the same Groq client for evaluation questions
_eval_client = OpenAI(
    base_url="https://api.groq.com/openai/v1",
    api_key=settings.groq_api_key,
)


@dataclass
class EvalExample:
    id: str
    question: str
    gold_answer: str
    gold_context_snippet: str  # short phrase or sentence that should appear in retrieved context
    where: Optional[Dict] = None


def load_eval_set(path: str) -> List[EvalExample]:
    """
    Expect a JSONL file where each line is:
    {
      "id": "...",
      "question": "...",
      "gold_answer": "...",
      "gold_context_snippet": "...",
      "where": {...}   // optional metadata filters
    }
    """
    items: List[EvalExample] = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            obj = json.loads(line)
            items.append(
                EvalExample(
                    id=obj["id"],
                    question=obj["question"],
                    gold_answer=obj["gold_answer"],
                    gold_context_snippet=obj["gold_context_snippet"],
                    where=obj.get("where"),
                )
            )
    return items


def compute_retrieval_recall(
    gold_snippet: str,
    retrieved_chunks: List[DocumentChunk],
) -> float:
    """
    Simple recall metric: 1.0 if any retrieved chunk contains the gold snippet (case-insensitive),
    else 0.0.
    """
    gold_lower = gold_snippet.lower()
    for c in retrieved_chunks:
        if gold_lower in c.text.lower():
            return 1.0
    return 0.0


def judge_faithfulness(
    question: str,
    answer: str,
    context_chunks: List[DocumentChunk],
) -> float:
    """
    Ask the LLM to rate faithfulness between 0 and 1.
    """
    context_texts = []
    for c in context_chunks:
        source = c.metadata.get("source", "unknown")
        context_texts.append(f"[source={source}] {c.text}")
    context_block = "\n\n".join(context_texts)

    prompt = f"""
You are evaluating a Retrieval-Augmented Generation (RAG) answer.

You will be given:
- A user question
- Retrieved context
- An answer generated by a model

Your task is to assign a faithfulness score between 0 and 1:
- 1 means the answer is completely supported by the context
- 0 means the answer is unsupported or contradicts the context
- Values in between indicate partial support.

Return ONLY a JSON object like:
{{"faithfulness": 0.0}}

Question:
{question}

Context:
{context_block}

Answer:
{answer}
"""

    completion = _eval_client.chat.completions.create(
        model=settings.llm_model,
        messages=[{"role": "user", "content": prompt}],
        temperature=0.0,
        max_tokens=128,
    )

    raw = completion.choices[0].message.content.strip()
    try:
        data = json.loads(raw)
        score = float(data.get("faithfulness", 0.0))
    except Exception:
        score = 0.0
    # clamp to [0, 1]
    score = max(0.0, min(1.0, score))
    return score


def run_eval(path: str, top_k: int = 5):
    eval_set = load_eval_set(path)

    total_recall = 0.0
    total_faithfulness = 0.0
    n = len(eval_set)

    for ex in eval_set:
        print(f"Evaluating example {ex.id}...")

        answer, chunks, _ = rag_query(
            ex.question,
            top_k=top_k,
            where=ex.where,
        )

        recall = compute_retrieval_recall(ex.gold_context_snippet, chunks)
        faithfulness = judge_faithfulness(ex.question, answer, chunks)

        total_recall += recall
        total_faithfulness += faithfulness

        print(f"  Recall: {recall:.2f}, Faithfulness: {faithfulness:.2f}")

    if n == 0:
        print("No examples in eval set.")
        return

    avg_recall = total_recall / n
    avg_faithfulness = total_faithfulness / n

    print("\n==== Evaluation Summary ====")
    print(f"Examples:          {n}")
    print(f"Average Recall:    {avg_recall:.3f}")
    print(f"Average Faithful.: {avg_faithfulness:.3f}")


if __name__ == "__main__":
    # Example usage:
    # python -m app.eval_rag ./data/eval_set.jsonl
    import sys

    if len(sys.argv) < 2:
        print("Usage: python -m app.eval_rag <path_to_eval_jsonl>")
        sys.exit(1)

    eval_path = sys.argv[1]
    run_eval(eval_path, top_k=5)